 <b>3. Правила и условия пользования ДИСКОНТНОЙ КАРТОЙ </b><br>
 Все клиенты, имеющие ДК, автоматически становятся Участниками Программы. Скидка по дисконтной карте начинает действовать с 1-й покупки. Скидка предоставляется только предъявителю карты. Дисконтная карта не является платежным средством. Срок действия дисконтной карты не ограничен. <br>
 Во время распродаж или действия специальных скидок, предложений и акций, на которые не распространяется скидка по ДК, сумма покупки на счет ДК не зачисляется. <br>
 Проценты, подлежащие начислению в результате использования ДК проводятся в течение 15 (пятнадцати) календарных дней. <br>
 Накопительная ДК является собственностью Оператора. <br>
 Оператор оставляет за собой право вносить изменения в условия обслуживания и предоставления скидок. <br>
 Участники Программы Оператора получают право на первоочередное, регулярное информирование об изменениях, распродажах и специальных акциях путем смс-оповещения и/или e-mail рассылки. <br>
