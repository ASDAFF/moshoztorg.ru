 <b>5. Возврат товара </b><br>
 Гарантийный возврат товаров, приобретенных с использованием ДК, осуществляется в порядке, предусмотренном действующим законодательством Российской Федерации. При возврате товара покупателю необходимости предъявлять дисконтную карту, которая использовалась при оформлении покупки. В случае если при приобретения товара был повышен процент скидки, при его возврате скидка автоматически возвращается на предыдущий процент скидки. <br>
